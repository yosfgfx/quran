// Get full path details
export function getPathById(pathId) {
    return THEMATIC_PATHS.find(p => p.id === pathId);
}

// Get specific day content
export function getPathDay(pathId, dayNumber) {
    const path = THEMATIC_PATHS.find(p => p.id === pathId);
    if (!path) return null;

    return path.days.find(d => d.day === dayNumber);
}

